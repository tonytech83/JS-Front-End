function smallestOfThreeNumbers(...numbers) {
  return Math.min(...numbers);
}

console.log(
  smallestOfThreeNumbers(
    2,
    5,
    3
  )
)

console.log(
  smallestOfThreeNumbers(
    600,
    342,
    123
  )
)

console.log(
  smallestOfThreeNumbers(
    25,
    21,
    4
  )
)

console.log(
  smallestOfThreeNumbers(
    2,
    2,
    2
  )
)